public class Numerico {
    public int Edad = 25;
    public double Salario = 3500.75;
    public float Altura = 1.78f;
    public long Telefono = 3124567890;
    public short Cantidad = 5;

    public int Sumar(int a, int b) => a + b;
    public double Promedio(double a, double b) => (a + b) / 2;
    public long Multiplicar(long a, long b) => a * b;
    public float ConvertirMetros(float km) => km * 1000f;
    public int Restar(int a, int b) => a - b;
}
